def convert_file(filename):
    """轉換單個檔案"""
    try:
        with open(filename, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # 進行轉換
        for simp, trad in replacements.items():
            content = content.replace(simp, trad)
        
        # 寫回檔案
        with open(filename, 'w', encoding='utf-8') as f:
            f.write(content)
        
        print(f"✓ 已轉換: {filename}")
        return True
    except Exception as e:
        print(f"✗ 錯誤 {filename}: {e}")
        return False

if __name__ == '__main__':
    if len(sys.argv) < 2:
        print("使用方式: python convert_complete.py <檔案1> [檔案2] ...")
        sys.exit(1)
    
    success_count = 0
    for filename in sys.argv[1:]:
        if convert_file(filename):
            success_count += 1
    
    print(f"\n完成! 成功轉換 {success_count}/{len(sys.argv)-1} 個檔案")
